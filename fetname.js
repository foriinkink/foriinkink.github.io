function fet_name_maker() {
  console.log("fet_name_maker() fired");
  
  // Array to hold the pieces of the fet_name we're building
  var fet_name_array = [];
  
  function getArrayRandom(arr) {
    var rand_from_arr = arr[Math.floor(Math.random() * arr.length)];
    return rand_from_arr;
  }
  
  if (document.getElementById("adjectives").checked) {
    var random_adjective = getArrayRandom(adjectives);
    var random_adjective_upper = CapitlizeString(random_adjective);
    fet_name_array.push(random_adjective_upper);
  }
  if (document.getElementById("body_adjectives").checked) {
    var random_body_adjective = getArrayRandom(body_adjectives);
    var random_body_adjective_upper = CapitlizeString(random_body_adjective);
    fet_name_array.push(random_body_adjective_upper);
  }
  if (document.getElementById("fetish").checked) {
    //var random_fetish = fetishes[Math.floor(Math.random() * fetishes.length)];
    var random_fetish = getArrayRandom(fetishes);
    var random_fetish_upper = CapitlizeString(random_fetish);
    fet_name_array.push(random_fetish_upper);
  }
  if (document.getElementById("role").checked) {
    var random_role = getArrayRandom(roles);
    var random_role_upper = CapitlizeString(random_role);
    fet_name_array.push(random_role_upper);
  }
  if (document.getElementById("animal").checked) {
    var random_animal = getArrayRandom(animals);
    var random_animal_upper = CapitlizeString(random_animal);
    fet_name_array.push(random_animal_upper);
  }
  if (document.getElementById("inet_slang").checked) {
    var random_inet_slang = getArrayRandom(inet_slang);
    fet_name_array.splice(fet_name_array.indexOf(fet_name_array[Math.floor(Math.random() * fet_name_array.length)]), 0, random_inet_slang);//
  }
  
  if (document.getElementById("random_order").checked) {
    fet_name_array = shuffle(fet_name_array);
  }
  
  fet_name = fet_name_array.join("");
  
  //https://stackoverflow.com/a/6274381
  function shuffle(a) {
    var j, x, i;
    for (i = a.length - 1; i > 0; i--) {
        j = Math.floor(Math.random() * (i + 1));
        x = a[i];
        a[i] = a[j];
        a[j] = x;
    }
    return a;
  }

  var fetname_textfield = document.getElementById("fetname_textfield");
  fetname_textfield.innerText = fet_name;
}
